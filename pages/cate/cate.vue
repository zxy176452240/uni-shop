<template>
  <view>
    <!-- 搜索区域 -->
    <my-search searchURL="/sub_packages/search/search"></my-search>
    <!-- 主体区域 -->
    <view class="scroll-view-container">
      <!-- 左侧滚动视图区域 -->
      <scroll-view scroll-y="true" class="left-scroll-view" :style="{height: wh + 'px'}">
       <block v-for="(item,index) in cateList" :key="index">
         <view :class="['left-scroll-view-item', index === active ? 'active' : '']" @click="activeChange(index)">{{item.cat_name}}</view>
        </block>
      </scroll-view>
      <!-- 右侧滚动视图区域 -->
      <scroll-view scroll-y="true" :scroll-top="scrollTop" class="right-scroll-view" :style="{height: wh + 'px'}">
        <!-- 二级分类 -->
        <view class="cate-lv2" v-for="(item2,index2) in cateLevel2" :key="index2">
          <view class="cate-lv2-title">/ {{item2.cat_name}} /</view>
          <!-- 三级分类 -->
          <view class="cate-lv3-list">
            <view class="cate-lv3-item" v-for="(item3,index3) in item2.children" :key="index3" @click="gotoGoodsList(item3)">
              <image :src="item3.cat_icon.replace('dev','web')"></image>
              <text>{{item3.cat_name}}</text>
            </view>
          </view>
        </view>
      </scroll-view>
    </view>
  </view>
</template>

<script>
  export default {
    data() {
      return {
        // 当前设备可用高度
        wh: 0,
        // 分类列表数据
        cateList:[],
        // 二级分类列表数据
        cateLevel2:[],
        // 当前选中项的索引
        active:0,
        // 滚动条距离顶部距离
        scrollTop:0
      };
    },
    onLoad() {
      // 获取当前设备可用高度
      const sysInfo = uni.getSystemInfoSync()
      // 减去搜索区域高度
      this.wh = sysInfo.windowHeight - 50
      // 获取分类列表数据
      this.getCateList()
    },
    methods: {
      async getCateList() {
        const {data:res}= await uni.$http.get('/api/public/v1/categories')
        // 请求失败
        if (res.meta.status !== 200) return uni.$showMsg()
        
        // 转存一级分类
        this.cateList = res.message
        // 转存二级分类
        this.cateLevel2 = res.message[0].children
      },
      
      // 切换active选中项事件
      activeChange(index) {
        this.active = index
        // 为二级分类列表重新赋值
        this.cateLevel2 = this.cateList[index].children
        // 重置距离顶部的距离
        // 此处每一次重置必须赋不同值,否则不触发吸顶
        this.scrollTop = Math.random()
      },
      
      // 跳转商品页面
      gotoGoodsList(item3) {
        uni.navigateTo({
          url:'/sub_packages/goods_list/goods_list?cid=' + item3.cat_id + '&name=' + item3.cat_name
        })
      }
    }
  }
</script>

